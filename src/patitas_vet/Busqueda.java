    public String getColumType(){
        try {
            String tabla = cmbTabla.getSelectedItem().toString();
            String colum = cmbColums.getSelectedItem().toString();
            System.out.println(tabla);
            System.out.println(colum);
            ConnectToServer bd = new ConnectToServer();
            String query = "SELECT DATA_TYPE FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = '"
                    + tabla
                    + "' and COLUMN_NAME = '"
                    + colum
                    + "'";
            System.out.println(query);
            bd.conectaSql();
            ResultSet rs = bd.querySql(query);
            ResultSetMetaData metadata = rs.getMetaData();
            int colums = metadata.getColumnCount();
            while (rs.next()) {
                Object fila = new Object();
                for (int i = 0; i < colums; i++) {
                    fila = rs.getObject(colums);
                }
                return fila.toString();
            }
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Busqueda.class.getName()).log(Level.SEVERE, null, ex);
        }
        return "None";
    }
    public void fillCmbColums(){
        try {
            String tabla = cmbTabla.getSelectedItem().toString();
            ConnectToServer bd = new ConnectToServer();
            DefaultComboBoxModel cmb = new DefaultComboBoxModel();
            String query = "select B.name from sysobjects A, syscolumns B where A.id = B.id and A.name = '" + tabla + "' order by B.colid";
            bd.conectaSql();
            ResultSet rs = bd.querySql(query);
            ResultSetMetaData metadata = rs.getMetaData();
            int colum = metadata.getColumnCount();
            Object nada = new Object();
            nada = "";
            cmb.addElement(nada);
            while (rs.next()) {
                Object fila = new Object();
                for (int i = 0; i < colum; i++) {
                    fila = rs.getObject(colum);
                }
                cmb.addElement(fila);
            }
            cmbColums.setModel(cmb);
            bd.cierraSql();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Busqueda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
