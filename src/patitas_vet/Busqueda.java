    public void fillTabla(){
        String tabla = cmbTabla.getSelectedItem().toString();
        try {
            String q = "select * from " + tabla;
            if(!"".equals(cmbColums.getSelectedItem().toString()) || !"".equals(txtBusqueda.getText().trim())){
                
                String name = cmbColums.getSelectedItem().toString();
                String search;
                if(txtBusqueda.isVisible()){
                    search = txtBusqueda.getText().trim();
                }else{
                    search = spinnerBusqueda.getValue().toString().trim();
                }
                
                if(!"".equals(search.trim())){
                   q = q + " where " + name + " = '" + search +"'"; 
                }
                
                
            }
            ConnectToServer bd = new ConnectToServer();
            bd.conectaSql();
            
            ResultSet rs = bd.querySql(q);
            ResultSetMetaData metadata = rs.getMetaData();
            int colum = metadata.getColumnCount();
            
            DefaultTableModel modelo = new DefaultTableModel(){
              @Override
              public boolean isCellEditable(int row, int column){
                  return false;
              }
            };
            for (int i = 1; i <= colum; i++) {
                modelo.addColumn(metadata.getColumnLabel(i));
            }
            
            //agregar los nombres a las filas
            while (rs.next()) {
                Object[] fila = new Object[colum];
                for (int i = 0; i < colum; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                modelo.addRow(fila);
            }
            tableBusqueda.setModel(modelo);
            tableBusqueda.getTableHeader().setReorderingAllowed(false);

            bd.cierraSql();
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
            Logger.getLogger(ConnectToServer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

