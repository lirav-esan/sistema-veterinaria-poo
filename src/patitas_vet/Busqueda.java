    public String[] getTableData(){
        int index = tableBusqueda.getSelectedRow();
        if (index == -1){
            JOptionPane.showMessageDialog(rootPane, "No hay fila seleccionada");
            
        }else{
            int rowLen = tableBusqueda.getColumnCount();
            String[] a = new String[rowLen];
            for (int i = 0; i < rowLen; i++){
                Object dato = tableBusqueda.getValueAt(index, i);
                a[i] = (dato != null)? dato.toString() : "";
            }
            return a;
        }
        return null;
    }
